#include <iostream>
using namespace std;

int main()
{
    cout << "5 + 3 = " << 5 + 3  << endl;
    cout << "5 - 3 = " << 5 - 3 << endl;
    cout << "5 * 3 = " << 5 * 3 << endl;
    cout << "5 / 3 = " << 5 / 3 << endl;
    cout << "5.0 / 3.0 = " << 5.0 / 3.0 << endl;
    cout << "5 % 3 = " << 5 % 3 << endl;
    cout << "6 % 3 = " << 6 % 3 << endl;
    cout << "5 * 3 + 2 = " << 5 * 3 + 2 << endl;
    cout << "5 * (3 + 2) = " << 5 * (3 + 2) << endl;
}
